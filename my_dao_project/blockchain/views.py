# Get an instance of the contract
contract = web3.eth.contract(address=contract_address, abi=abi)

# View to get the name, symbol, total supply, and treasury address of the DAO
def get_dao_info(request):
    name = contract.functions.name().call()
    symbol = contract.functions.symbol().call()
    total_supply = contract.functions.totalSupply().call()
    treasury = contract.functions.treasury().call()

    return JsonResponse({
        "name": name,
        "symbol": symbol,
        "total_supply": total_supply,
        "treasury": treasury
    })

# View to get details of all proposals
def get_all_proposals(request):
    proposals_count = len(contract.functions.proposals().call())
    proposals = []
    
    for i in range(proposals_count):
        proposal = contract.functions.proposals(i).call()
        proposals.append({
            "index": i,
            "description": proposal[0],
            "deadline": proposal[1],
            "yesVotes": proposal[2],
            "noVotes": proposal[3],
            "executed": proposal[4],
        })

    return JsonResponse({"proposals": proposals})

# View to get details of a single proposal by its index
def get_proposal(request, proposal_index):
    proposal = contract.functions.proposals(proposal_index).call()
    return JsonResponse({
        "description": proposal[0],
        "deadline": proposal[1],
        "yesVotes": proposal[2],
        "noVotes": proposal[3],
        "executed": proposal[4],
    })

# View to get quorum percentage and voting duration
def get_voting_params(request):
    quorum_percentage = contract.functions.quorumPercentage().call()
    voting_duration = contract.functions.votingDuration().call()
    
    return JsonResponse({
        "quorum_percentage": quorum_percentage,
        "voting_duration": voting_duration
    })

